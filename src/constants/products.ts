// Helper functions
export const getProductsByCategory = (category: string): Product[] => {
  return products.filter((p) => p.category === category);
};

export const getProductBySlug = (slug: string): Product | undefined => {
  return products.find((p) => p.slug === slug);
};

export const getFeaturedProducts = (limit: number = 12): Product[] => {
  return products.filter((p) => p.badge).slice(0, limit);
};

export const getNewArrivals = (limit: number = 12): Product[] => {
  return products.filter((p) => p.badge === "New Arrival").slice(0, limit);
};

export const getBestSellers = (limit: number = 12): Product[] => {
  return products.filter((p) => p.badge === "Best Seller").slice(0, limit);
};
